package test;

public class MoveSmall {
	public static int x = 1;
	public static int y = 1;
	public static int firstIndex;
	public static int sumIndex = 10;

	public int getX() {
		return x;
	}

	public static void setX(int x) {
		x++;
	}

	public int getY() {
		return y;
	}

	public static void setY(int y) {
		y++;
	}

	public int getFirstIndex() {
		return firstIndex;
	}

	public static void setFirstIndex(int firstIndex) {
		firstIndex++;
	}

	public int getSumIndex() {
		return sumIndex;
	}

}
